package personal;

public class CharTimes {
    private char c;
    private int time;

    public CharTimes(char c) {
        this.c = c;
        this.time = 0;
    }

    public char getC() {
        return c;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
}
