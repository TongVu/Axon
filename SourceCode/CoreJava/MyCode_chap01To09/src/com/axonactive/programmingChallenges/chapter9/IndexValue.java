package com.axonactive.programmingChallenges.chapter9;

public class IndexValue {
    private int index = 0;
    private int value = 0;

    public IndexValue() {
        this.index = 0;
        this.value = 0;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

}
